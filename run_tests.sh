#!/bin/bash
# ===== COMPLETE TEST EXECUTION SCRIPT (Linux/Mac) =====
# Purpose: Build project and run all tests
# Usage: chmod +x run_tests.sh && ./run_tests.sh

set -e  # Exit on error

cd "$(dirname "$0")"

echo ""
echo "??????????????????????????????????????????????????????????"
echo "?       BULLET HOLE DETECTION SYSTEM - TEST SUITE        ?"
echo "?              Building and Testing...                   ?"
echo "??????????????????????????????????????????????????????????"
echo ""

# Check if build directory exists
if [ ! -d "build" ]; then
    echo "[INFO] Creating build directory..."
    mkdir -p build
fi

cd build

# Step 1: CMake Configuration
echo "[STEP 1] Configuring CMake..."
cmake .. -DCMAKE_BUILD_TYPE=Release

if [ $? -ne 0 ]; then
    echo "[ERROR] CMake configuration failed!"
    exit 1
fi

echo "[SUCCESS] CMake configuration complete"
echo ""

# Step 2: Build all targets
echo "[STEP 2] Building all targets..."
cmake --build . --config Release --parallel $(nproc 2>/dev/null || echo 4)

if [ $? -ne 0 ]; then
    echo "[ERROR] Build failed!"
    exit 1
fi

echo "[SUCCESS] Build complete"
echo ""

# Step 3: Run Unit Tests
echo "[STEP 3] Running STEP 2 & 3 Unit Tests..."
echo "????????????????????????????????????????????????????????"

if [ -f "./test_step2_step3_unit" ]; then
    ./test_step2_step3_unit
    if [ $? -ne 0 ]; then
        echo "[WARNING] Unit test failed, but continuing..."
    fi
else
    echo "[WARNING] Unit test executable not found"
fi

echo ""

# Step 4: Run Integration Tests
echo "[STEP 4] Running Integration Tests..."
echo "????????????????????????????????????????????????????????"

if [ -f "./test_integration_real_image" ]; then
    ./test_integration_real_image
    if [ $? -ne 0 ]; then
        echo "[WARNING] Integration test failed, but continuing..."
    fi
else
    echo "[WARNING] Integration test executable not found"
fi

echo ""

# Step 5: Run CTest
echo "[STEP 5] Running CTest..."
echo "????????????????????????????????????????????????????????"
ctest --output-on-failure || true

echo ""

echo "??????????????????????????????????????????????????????????"
echo "?              TEST EXECUTION COMPLETE                   ?"
echo "?                                                        ?"
echo "? Next steps:                                            ?"
echo "? 1. Review test outputs above                           ?"
echo "? 2. Check for any errors or warnings                    ?"
echo "? 3. Run VALIDATION_AND_METRICS_GUIDE.md if needed      ?"
echo "? 4. Deploy to production when ready                     ?"
echo "??????????????????????????????????????????????????????????"
echo ""
